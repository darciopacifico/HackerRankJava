    private static int leftMost(int[][] arr) {

        int leftMost = arr.length;
        for (int i = 0; i < arr.length; i++)
            leftMost = Math.min(leftMost(arr[i], 0, Math.min(leftMost, arr.length - 1)), leftMost);

        if (leftMost == arr.length)
            return -1;

        return leftMost;
    }

    private static int leftMost(int[] arr, int start, int end) {

        if (arr[start] == 0 && arr[end] == 0) {
            return arr.length;
        }

        if (start == end) {
            return start;
        }

        int mid = start + ((end - start) / 2);

        if (arr[mid] == 0) {

            return leftMost(arr, mid + 1, end);
        } else {
            return leftMost(arr, start, mid);
        }
    }


